public class PalavrasReservadas {
    public static void main(String[] args) {
        /*PALAVRAS RESERVADAS
        *
        * CONTROLE DE PACOTES
        * import
        * package
        *
        * MODIFICADORES DE ACESSO
        * public - Acesso de qualquer classe
        * private - Acesso apenas dentro da classe
        * protected - Acesso por classes no mesmo pacote e subclasses
        *
        * PRIMITIVOS
        * boolean
        * byte
        * char
        * double
        * float
        * int
        * long
        * short
        * void
        *
        * MODIFICADORES DE CLASSES, VARIÁVEIS OU MÉTODOS
        *
        * abstract - classe que nao pode ser instanciada ou metodo que precisa ser implementado por uma subclasse nao abstrata
        * class - especifica uma classe
        * extends - indica a superclasse que a subclasse está estendendo
        * final - impossibilita que uma classe seja estendida, que um metodo seja sobrescrito ou que uma variável seja reiniciada
        * implements - indica as interfaces que uma classe irá implementar
        * interface - especifica uma interface
        * native - indica que um metodo está escrito em uma linguagem dependente de plataforma, como o C
        * new instancia um novo objeto, chamando seu construtor
        * static - faz um método ou variável pertencer à classe ao invés de às instancias
        * strictfp - indica que os numeros de ponto flutuante de um metodo ou classe seguirao as regras em todas as expressoes
        * synchronized - indica que um metodo só pode ser acessado por uma thread de cada vez
        * transient - impede a serializacao de campos
        * volatile - indica que uma variavel pode ser alterada durante o uso de threads
        *
        * CONTROLE DE FLOXO DENTRO DE UM BLOCO DE CÓDIGO
        * break - sai do bloco de codigo em que ele está
        * case - executa um bloco de codigo dependendo do teste do switch
        * continue - pula a execucao do codigo que viria apos essa linha e vai para a próxima passagem do loop
        *
        * throw - usado para chamar uma excecao para o metodo que o chamou
        * throws - indica que um metodo pode passar uma excecao para o metodo que o chamou
        * try - bloco de codigo que tentara ser executado, mas que pode causar uma excecao
        *
        * VARIAVEIS DE REFERENCIA
        *
        * super refere-se a superclasse imediata
        * this - refere-se a instancia atual do objeto
        *
        *
        * */
    }
}
