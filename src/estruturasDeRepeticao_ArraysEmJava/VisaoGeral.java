package estruturasDeRepeticao_ArraysEmJava;

public class VisaoGeral {
    public static void main(String[] args) {
        /*
        * ESTRUTURA DE REPETIÇÃO
        *
        * WHILE
        *   while(condicao){
        *       instrucoes
        *       incremento/decremento
        *   }
        * ----------------------------------------
        * DO-WHILE
        *   do{
        *       instruçoes
        *       incremento/decremento
        *   }while(condicao);
        * -----------------------------------------
        * FOR(valor inicial; condicao; incremento)
        *   for(int i = 0; i < 10; i++ ) {
        *       instrucoes
        *   }
        * -----------------------------------------
        * OPERACOES ARITMÉTICAS
        *
        * variavel += valor;
        * variavel -= valor;
        * variavel *= valor;
        * variavel /= valor;
        * -----------------------------------------
        * ARRAYS - objeto para armazenar sequencialmente dados do mesmo tipo
        *   Array unidimensional  -     a[0], a[1], a[2], a[3], a[4], a[5], a[...]
        *   Array multidimensional -    am[0][0] am[0][1] am[0][...]
        *                               am[1][0] am[1][1] am[1][...]
        *                               am[...][0] ...
        *   Estrutura   -     nomeArray[indiceLinha][indiceColuna]
        * */
    }
}
